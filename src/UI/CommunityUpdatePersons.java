    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblPersons.getModel();
        dtm.setRowCount(0);
        
    
        for(Person person: this.persondirectory.getPersonHistory()){
            Object[] row = new Object[5];
            row[0] = person;
            row[1]= Integer.valueOf(person.getAge());
            row[2]= person.getAddress();
            row[3]= person.getCommunity();
            row[4]= person.getCity();
            
            dtm.addRow(row);
   }
    }

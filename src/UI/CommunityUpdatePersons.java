    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        txtName.setText("");
        txtAge.setText("");
        txtAddress.setText("");
        txtCommunity.setText("");
        txtCity.setText("");

        String FindName = txtFindPerson.getText();
        int found=0;
        txtFindPerson.setText("");
        for(Person person : this.persondirectory.getPersonHistory())
        {
            if(FindName.trim().equals(person.getName().trim()))
            {
                txtName.setText(person.getName());
                txtAge.setText(String.valueOf(person.getAge()));
                txtAddress.setText(person.getAddress());
                txtCommunity.setText(person.getCommunity());              
                txtCity.setText(person.getCity());
                found=1;
                System.out.print(person.getName()+" Search success!");
            }
        }
        if(found==0)
        {
            JOptionPane.showMessageDialog(this,"No Person found with the given name! Try again!");
            return;
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int SelectedRowIndex = tblPersons.getSelectedRow();

        if(SelectedRowIndex<0)
        {
            JOptionPane.showMessageDialog(this,"Please select a row to update");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblPersons.getModel();
        Person selectedPerson = (Person) model.getValueAt(SelectedRowIndex,0);

        int failsafe=0;
        for (Person personiterator : persondirectory.getPersonHistory())
        {
            if( (personiterator.getName().equals(txtName.getText().isEmpty()?"":txtName.getText())) && (!personiterator.getName().equals(selectedPerson.getName())))
            {
                JOptionPane.showMessageDialog(this, "Name must be unique!");
                failsafe=1;
            }

        }
        if(txtName.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Name must not be Empty!");
            failsafe=1;
        }

        if(txtCity.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "City must not be Empty!");
            failsafe=1;
        }
        if(txtAddress.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Address must not be Empty!");
            failsafe=1;
        }


        if(txtAge.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Age must not be Empty!");
            failsafe=1;
        }
        
                if(txtCommunity.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Age must not be Empty!");
            failsafe=1;
        }

        if(txtAddress.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Age must not be Empty!");
            failsafe=1;
        }

        if(failsafe==0)
        {
            selectedPerson.setName(txtName.getText());
            selectedPerson.setCity(txtCity.getText());
            selectedPerson.setCommunity(txtCommunity.getText());
            selectedPerson.setAge(Integer.valueOf(txtAge.getText()));
            selectedPerson.setAddress(txtAddress.getText());
            SimpleDateFormat formatter = new SimpleDateFormat("MM-dd-yyyy HH:mm:ss");
            selectedPerson.setUpdateDate(formatter.format(new Date()));
            JOptionPane.showMessageDialog(this, "Details updated successfully!");
        }
        populateTable();
    }//GEN-LAST:event_btnUpdateActionPerformed


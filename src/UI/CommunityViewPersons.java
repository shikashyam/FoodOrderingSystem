      private void populateTable(ArrayList<Person> persondirectory,String community) {
          
        DefaultTableModel dtm1 = (DefaultTableModel) tblHealthyPeople.getModel();
        DefaultTableModel dtm2 = (DefaultTableModel) tblUnhealthyPeople.getModel();
        dtm1.setRowCount(0);
        dtm2.setRowCount(0);
        int healthy=0;
        int unhealthy=0;
        
        for(Person person: persondirectory){
           // Patient patient=person.getPatient();
            if(person.getCommunity().equals(community))
            {
            Object[] row = new Object[4];
            row[0] = person;
            row[1]= String.valueOf(person.getAge());
            row[2]= person.getAddress();
            row[3]= person.getCommunity();
            
            if("Normal".equals(person.getHealth().trim()))
            {dtm1.addRow(row);
            healthy++;
            }
            
            else
            {
             dtm2.addRow(row);
             unhealthy++;       
            }
            }}
        
        txtHealthyCounts.setText(String.valueOf(healthy));
        txtUnhealthyCounts.setText(String.valueOf(unhealthy));
        this.HealthyCount=healthy;
        this.UnhealthyCount=unhealthy;
      }
       

       pieDataSet.setValue("Abnormal Vital Signs",UnhealthyCount);

       
       JFreeChart piechart = ChartFactory.createPieChart("Health Trends of Community",pieDataSet,true,true,true);
       PiePlot P=(PiePlot)piechart.getPlot();
       //P.setForegroundAlpha(TOP_ALIGNMENT);
       ChartFrame frame = new ChartFrame("Health Trends",piechart);
       frame.setVisible(true);
